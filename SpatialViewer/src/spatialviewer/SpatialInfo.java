package spatialviewer;

public class SpatialInfo {
    private Object translation, rotation, scale;

    /**
     * @return the translation
     */
    public Object getTranslation() {
        return translation;
    }

    /**
     * @param translation the translation to set
     */
    public void setTranslation(Object translation) {
        this.translation = translation;
    }

    /**
     * @return the rotation
     */
    public Object getRotation() {
        return rotation;
    }

    /**
     * @param rotation the rotation to set
     */
    public void setRotation(Object rotation) {
        this.rotation = rotation;
    }

    /**
     * @return the scale
     */
    public Object getScale() {
        return scale;
    }

    /**
     * @param scale the scale to set
     */
    public void setScale(Object scale) {
        this.scale = scale;
    }
}
